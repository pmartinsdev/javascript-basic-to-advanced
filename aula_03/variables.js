// Variaveis Atribuem valor [var, let, const]

// VAR - Variável ou seja ela pode ser alterada.
var fruta = "Maça";
console.log(fruta); // Maça

fruta = "Banana";
console.log(fruta); // Banana

// let - Let vem de definir, e também é uma variavel que pode ser alterada.
let livro = "Clean Code";
console.log(livro); // Clean Code

livro = null;
console.log(livro); // null

// const - Const vem de constante, ou seja é o único que não pode ser alterado depois de definido.
const nome = "Paulo César";
console.log(nome); // Paulo César

nome = "Renata"; // Receberei um erro por tentar re-definir uma constante.
